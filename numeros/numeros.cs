// Números Inteiros
// Definições 
// - Números inteiros, ou seja, sem pontuação, podem ser definidas pelos tipos
// - short/ushort
// - int/uint
// - long/ulong
// - Assim como temos o signed nos bytes, nos números por padrão são permitidos valores negativos
// - Então usamos o unsigned para definir que o mesmo não pode receber valores negativos
// - O tipo int é o mais comum a ser encontrado

// short (16-bit)
// - -32,768 to 32,767
// ushort (16-bit)
// 0 até 65.535
// int (32-bit)
// -2,147,483,648 até 2,147,483,647
// uint (32-bit)
// 0 até 4,294,967,295
// long (64-bit)
// -9,223,372,036,854,775,808 ate 9,223,372,036,854,775,807
// ulong (64-bit)
// 0 ate 18,446, 744,073,709,551,615